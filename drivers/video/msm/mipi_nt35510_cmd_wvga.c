int mipi_nt35510_set_dynamic_gamma(enum danymic_gamma_mode  gamma_mode,struct msm_fb_data_type *mfd)
{
	int ret = 0;

	if((!nt35510_gamma_25)||(!nt35510_gamma_22))
	{
		LCD_DEBUG("%s:don't support dynamic_gamma\n",__func__);
		return -EPERM;
	}

#ifdef CONFIG_HW_ESD_DETECT
	if (!mfd->is_panel_alive)
	{
		LCD_DEBUG("%s: panel not alive.\n", __func__);
		return -EPERM;
	}
#endif

	switch(gamma_mode)
	{
		case GAMMA25:
			process_mipi_table(mfd,&nt35510_tx_buf,nt35510_gamma_25,
						gamma_25_len, lcd_panel_wvga);
			break;
		case GAMMA22:
			process_mipi_table(mfd,&nt35510_tx_buf,nt35510_gamma_22,
						gamma_22_len, lcd_panel_wvga);
			break;
		default:
			LCD_DEBUG("%s: invalid dynamic_gamma: %d\n", __func__,gamma_mode);
			ret = -EINVAL;
			break;
	}
	LCD_DEBUG("%s: change gamma mode to %d\n",__func__,gamma_mode);
	return ret;
}

