static void panel_nt35510_check_live_status_cb(char* data)
{
#ifdef CONFIG_FB_DISPLAY_INVERSION
	int invar_result = save_display_inversion_state?0x20:0x0;
#else
	int invar_result = 0x0;
#endif
	if( (cmdreq.cmds==NULL)||(cmdreq.cmds->payload==NULL) ||(data==NULL) )
	{
		LCD_DEBUG("%s: Fatal error,NULL pointer\n", __func__);
		return;
	}
	switch(cmdreq.cmds->payload[0])
	{
		case 0x0A: /*Power mode command result*/
		{
			if(unlikely(data[0] != 0x9C))
			{
				printk("panel ic error: in %s,  reg 0x%02X should be 0x9C, but read data =0x%x \n",__func__,cmdreq.cmds->payload[0],data[0]);
				panel_read_flag = PANEL_READ_ERROR;
			}
			else
			{
				panel_read_flag = PANEL_READ_OK;
			}
			break;
		}
		case 0x0B: /*MADCTR command result*/
		{
			if(unlikely(data[0] != 0x0))
			{
				printk("panel ic error: in %s,  reg 0x%02X should be 0x0, but read data =0x%x \n",__func__,cmdreq.cmds->payload[0],data[0]);
				panel_read_flag = PANEL_READ_ERROR;
			}
			else
			{
				panel_read_flag = PANEL_READ_OK;
			}
			break;
		}
		case 0x0C: /*pixel format result*/
		{
			if(unlikely(data[0] != 0x07))
			{
				printk("panel ic error: in %s,  reg 0x%02X should be 0x07, but read data =0x%x \n",__func__,cmdreq.cmds->payload[0],data[0]);
				panel_read_flag = PANEL_READ_ERROR;
			}
			else
			{
				panel_read_flag = PANEL_READ_OK;
			}
			break;
		}
		case 0x0D: /*image mode result*/
		{
			if(unlikely(data[0] != invar_result))
			{
				printk("panel ic error: in %s,  reg 0x%02X should be 0x%02X, but read data =0x%x \n",__func__,cmdreq.cmds->payload[0],invar_result,data[0]);
				panel_read_flag = PANEL_READ_ERROR;
			}
			else
			{
				panel_read_flag = PANEL_READ_OK;
			}
			break;
		}
		default:
			panel_read_flag = PANEL_READ_OK;
			break;
	}
}

static void send_req_cmd(struct dsi_cmd_desc *dsi_cmd, int rec_len)
{
	cmdreq.cmds = dsi_cmd;
	cmdreq.cmds_cnt = 1;
	cmdreq.flags = CMD_REQ_RX | CMD_REQ_COMMIT | CMD_CLK_CTRL;
	cmdreq.rlen = rec_len;/*read 1 byte*/
	cmdreq.cb_hw= panel_nt35510_check_live_status_cb; 
	mipi_dsi_cmdlist_put(&cmdreq);
}

/*if Panel IC works well, return 1, else return 0 */
static int panel_nt35510_check_live_status(struct msm_fb_data_type *mfd) 
{
	int i = 0;
	int noerr = 0;
	#define REPEAT_COUNT 5
	if (!mfd->is_panel_alive)
	{
		LCD_DEBUG("%s: panel not alive.\n", __func__);
		return noerr;
	}

	/*check for few times if we can pass or not*/
	for (i=0; i<REPEAT_COUNT; i++)
	{
		send_req_cmd(&nt35510_read_display_power_mode_cmd,1);
		if(likely(panel_read_flag == PANEL_READ_OK))
		{
			noerr = 1;
			break;
		}
		else
		{
			noerr = 0;
			if(i == REPEAT_COUNT-1)
			{
				return noerr;
			}
		}
	}

	for (i=0; i<REPEAT_COUNT; i++)
	{
		send_req_cmd(&nt35510_read_display_MADCTR_cmd,1);
		if(likely(panel_read_flag == PANEL_READ_OK))
		{
			noerr = 1;
			break;
		}
		else
		{
			noerr = 0;
			if(i == REPEAT_COUNT-1)
			{
				return noerr;
			}
		}
	}

	for (i=0; i<REPEAT_COUNT; i++)
	{
		send_req_cmd(&nt35510_read_display_pixel_format_cmd,1);
		if(likely(panel_read_flag == PANEL_READ_OK))
		{	
			noerr = 1;
			break;
		}
		else
		{
			noerr = 0;
			if(i == REPEAT_COUNT-1)
			{
				return noerr;
			}
		}
	}

	for (i=0; i<REPEAT_COUNT; i++)
	{
		send_req_cmd(&nt35510_read_display_image_mode_cmd,1);
		if(likely(panel_read_flag == PANEL_READ_OK))
		{	
			noerr = 1;
			break;
		}
		else
		{
			noerr = 0;
			if(i == REPEAT_COUNT-1)
			{
				return noerr;
			}
		}
	}

	return noerr;
}
